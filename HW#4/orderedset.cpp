//this is our struct which stores an integer value and same struct pointer to point to next one
struct element {
  int val;
  element * next;

  element(int val_ = 0, element *next_ = nullptr) : val(val_), next(next_) {} //this is constructor of this structure, act bth as default constructor and otherwise
};

