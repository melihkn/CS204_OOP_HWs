    LinkedListSet & operator += (LinkedListSet & rhs) {
      element *rhs_temp_head = rhs.head;
      element *lhs_temp_head = this->head;

      //iterate all the values of rhs list and add to the lhs list
      while (rhs_temp_head) {
        *this += rhs_temp_head->val;
        rhs_temp_head = rhs_temp_head->next;
      }
      return *this;
    }

    //this operator overload allows user to delete an element from the list
    LinkedListSet & operator - (const int & value_to_remove) {
      element * temp_head = (*this).head;
      element * element_to_be_deleted = nullptr;
      
      //check whether first one will be deleted or not, in order to change head pointer and preserve the beginng of the list
      if (temp_head && temp_head->val == value_to_remove) {
        head = temp_head->next;
        element_to_be_deleted = temp_head;
        delete element_to_be_deleted;
        element_to_be_deleted = nullptr;
      }
      else {
        //otherwise iterate all over the list to find tobedeleted one and delete it
        while (temp_head && temp_head->next) {
          if (temp_head->next->val == value_to_remove) {
            element_to_be_deleted = temp_head->next;
            temp_head->next = temp_head->next->next;
            delete element_to_be_deleted;
            break;
          }
          else {
            temp_head = temp_head->next;
          }
        }
      }
      return *this;
    }

    //this operator overload allow user to delete values from lhs which also in rhs list (basically set difference)
    LinkedListSet & operator -= (const LinkedListSet & rhs) {
      element * rhs_temp = rhs.head;

      while (rhs_temp) {
        (*this) - rhs_temp->val;        
        rhs_temp = rhs_temp->next;
      }
      return *this;
    }

    //this function returns true when rhs value is in lhs linkedlistset object, return false otherwise
    bool value_is_in (const LinkedListSet & lhs, const int & rhs) {
      element * lhs_temp = lhs.head;

      while (lhs_temp) {
        if (lhs_temp->val == rhs) return true;
        lhs_temp = lhs_temp->next;
      }

      return false;
    }

    //this operator overloading allows user to find intersection of two linkedlistset objects
